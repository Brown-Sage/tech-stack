#     EXPLICIT TYPE CASTING(MANUAL TYPECASTING)
a = "12"
b = "23"
# print(a+b)
print("type of a and b is ",type(a),type(b))
print(int(a)+int(b))         #This is explicit typecasting

# IMPLICIT TYPECASTING(AUTOMATIC TYPECASTING)
c = 1.9
d = 2
print ("type of c is ",type(c),"and type of d is ",type(d))
print(c+d)                   #This is implicit typecasting 