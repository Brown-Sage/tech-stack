 #Arithmetic op
print(5/2)
print(5//2)
print(5*2)
print(5**2)
print(5+2)      


#Comparison op
print(2<3)
print(2==3)
print(2<=3)
print(2!=2)

#Logical op
print(2<1 or 2<3) #both should be true or false else true will occur
print(2<1 and 2<3) #both should be true or false else false will occur
print(not 2<3) #it show false if its true and true if false